exports.feedback = function (lang, type) {
    type = type.toLowerCase();
    lang = lang.toLowerCase();
    return (feedback[type] && (feedback[type][lang] || feedback[type].en))
        || type.toUpperCase() + " Feedback";
};

exports.tr = function (lang, identifier, vars) {
    identifier = identifier.toLowerCase();
    lang = lang.toLowerCase();

    if(vars) {
        return replaceVars(identifier, vars);
    }

    return (messages[identifier] && (messages[identifier][lang] || messages[identifier].en))
        || identifier.toUpperCase();

    function replaceVars(identifier, vars) {
        var message = messages[identifier] && (messages[identifier][lang] || messages[identifier].en);

        return message.split(' ').map(function(currentValue) {
            if(currentValue.indexOf('{{')!== -1){
                $.each(Object.keys(vars), function(index, varName) {
                    if(currentValue.indexOf(varName)!== -1) {
                        currentValue = vars[varName];
                    }
                })

            }

            return currentValue;
        }).join(' ');
    }
};

